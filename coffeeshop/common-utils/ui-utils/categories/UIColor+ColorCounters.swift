import UIKit

extension UIColor {
    
    class func baseColorCounters() -> UIColor{
        return UIColor(red: 255/255.0, green: 149/255.0, blue: 0, alpha: 1.0)
    }
    
    class func zeroColorCounter() -> UIColor{
        return UIColor(red: 237/255.0, green: 237/255.0, blue: 237/255.0, alpha: 1.0)
    }
    
}
